#!/usr/bin/env python3
"""
Run benchmark iterations generated by setup_benchmark.py.

This runner executes `codex exec` inside each run directory, using a bounded
rolling queue that keeps the total number of live processes under a configured
limit. The canonical scheduling unit is an eval pair:

- with_skill/run-K
- without_skill/run-K or old_skill/run-K

The runner records:
- codex-events.jsonl
- codex-stderr.log
- timing.json
- run_provenance.json
"""

from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
import time
from collections import deque
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import BinaryIO

try:
    from scripts.utils import apply_codex_model_settings
except ModuleNotFoundError:
    from utils import apply_codex_model_settings


UTF8 = "utf-8"
DEFAULT_MODEL = "gpt-5.4-mini"
DEFAULT_SANDBOX = "workspace-write"
DEFAULT_MAX_CONCURRENCY = 6
DEFAULT_POLL_INTERVAL = 0.5
CONFIG_PRIORITY = {
    "with_skill": 0,
    "without_skill": 1,
    "old_skill": 1,
}


def read_json_utf8(path: Path) -> dict:
    with path.open(encoding=UTF8) as handle:
        return json.load(handle)


def write_json_utf8(path: Path, data: object) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding=UTF8) as handle:
        json.dump(data, handle, indent=2, ensure_ascii=False)
        handle.write("\n")


def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def isoformat_utc(value: datetime) -> str:
    return value.strftime("%Y-%m-%dT%H:%M:%SZ")


def parse_numeric_suffix(path: Path, prefix: str) -> int:
    if not path.name.startswith(prefix):
        raise ValueError(f"Expected name starting with '{prefix}': {path.name}")
    suffix = path.name[len(prefix):]
    return int(suffix)


def config_sort_key(name: str) -> tuple[int, str]:
    return (CONFIG_PRIORITY.get(name, 9), name)


def validate_max_concurrency(value: int) -> int:
    if value < 1:
        raise ValueError("max_concurrency must be at least 1")
    return value


def validate_poll_interval(value: float) -> float:
    if value <= 0:
        raise ValueError("poll_interval must be greater than 0")
    return value


def resolve_codex_bin(command: str) -> str:
    resolved = shutil.which(command)
    if resolved:
        return resolved

    path = Path(command)
    if path.exists():
        return str(path.resolve())

    raise FileNotFoundError(f"Unable to resolve codex executable: {command}")


def discover_skill_path(run_dir: Path) -> str | None:
    skill_root = run_dir / ".agents" / "skills"
    if not skill_root.exists():
        return None

    children = sorted(child for child in skill_root.iterdir() if child.is_dir())
    if not children:
        return str(skill_root)

    return str(children[0])


def extract_tokens_from_events(events_path: Path) -> int:
    if not events_path.exists():
        return 0

    token_candidates: list[int] = []

    def collect_candidates(node: object) -> None:
        if isinstance(node, dict):
            direct_total = node.get("total_tokens")
            if isinstance(direct_total, (int, float)):
                token_candidates.append(int(direct_total))

            alt_total = node.get("totalTokens")
            if isinstance(alt_total, (int, float)):
                token_candidates.append(int(alt_total))

            alt_snake = node.get("total_token_count")
            if isinstance(alt_snake, (int, float)):
                token_candidates.append(int(alt_snake))

            input_tokens = node.get("input_tokens")
            output_tokens = node.get("output_tokens")
            if isinstance(input_tokens, (int, float)) and isinstance(output_tokens, (int, float)):
                token_candidates.append(int(input_tokens + output_tokens))

            prompt_tokens = node.get("prompt_tokens")
            completion_tokens = node.get("completion_tokens")
            if isinstance(prompt_tokens, (int, float)) and isinstance(completion_tokens, (int, float)):
                token_candidates.append(int(prompt_tokens + completion_tokens))

            for child in node.values():
                collect_candidates(child)
        elif isinstance(node, list):
            for item in node:
                collect_candidates(item)

    with events_path.open("r", encoding=UTF8, errors="replace") as handle:
        for line in handle:
            line = line.strip()
            if not line:
                continue
            try:
                payload = json.loads(line)
            except json.JSONDecodeError:
                continue
            collect_candidates(payload)

    return max(token_candidates, default=0)


@dataclass(frozen=True)
class RunSpec:
    eval_id: int
    eval_name: str
    config: str
    run_number: int
    run_dir: Path
    prompt_path: Path
    baseline_type: str

    @property
    def label(self) -> str:
        return f"eval-{self.eval_id}/{self.config}/run-{self.run_number}"

    @property
    def events_path(self) -> Path:
        return self.run_dir / "codex-events.jsonl"

    @property
    def stderr_path(self) -> Path:
        return self.run_dir / "codex-stderr.log"

    @property
    def timing_path(self) -> Path:
        return self.run_dir / "timing.json"

    @property
    def provenance_path(self) -> Path:
        return self.run_dir / "run_provenance.json"


@dataclass(frozen=True)
class RunPair:
    eval_id: int
    eval_name: str
    run_number: int
    runs: tuple[RunSpec, ...]

    @property
    def label(self) -> str:
        return f"eval-{self.eval_id}/run-{self.run_number}"

    @property
    def size(self) -> int:
        return len(self.runs)


@dataclass
class ActiveRun:
    spec: RunSpec
    command: list[str]
    resolved_reasoning_effort: str | None
    process: subprocess.Popen[bytes]
    prompt_handle: BinaryIO
    stdout_handle: BinaryIO
    stderr_handle: BinaryIO
    start_dt: datetime
    start_perf: float


@dataclass(frozen=True)
class CompletedRun:
    spec: RunSpec
    returncode: int
    duration_seconds: float
    total_tokens: int


def build_codex_command(
    codex_bin: str,
    run_dir: Path,
    model: str,
    reasoning_effort: str | None,
    sandbox: str,
) -> tuple[list[str], str | None]:
    command = [codex_bin, "exec", "-"]
    resolved_reasoning_effort = apply_codex_model_settings(command, model, reasoning_effort)
    command.extend(
        [
            "--cd",
            str(run_dir),
            "--sandbox",
            sandbox,
            "--skip-git-repo-check",
            "--json",
            "--ephemeral",
        ]
    )
    return command, resolved_reasoning_effort


def infer_baseline(iteration_dir: Path, metadata: dict) -> str:
    baseline = metadata.get("baseline")
    if isinstance(baseline, str) and baseline:
        return baseline

    sample_eval = next((path for path in sorted(iteration_dir.glob("eval-*")) if path.is_dir()), None)
    if sample_eval is None:
        raise FileNotFoundError(f"No eval directories found in {iteration_dir}")

    config_dirs = {
        path.name
        for path in sample_eval.iterdir()
        if path.is_dir() and any(child.is_dir() for child in path.glob("run-*"))
    }
    for candidate in ("without_skill", "old_skill"):
        if candidate in config_dirs:
            return candidate

    raise ValueError(f"Unable to infer baseline config in {sample_eval}")


def is_successful_run(spec: RunSpec) -> bool:
    if not spec.timing_path.exists():
        return False

    try:
        timing = read_json_utf8(spec.timing_path)
    except (OSError, json.JSONDecodeError):
        return False

    return timing.get("exit_code") == 0


def discover_pairs(iteration_dir: Path, resume: bool) -> list[RunPair]:
    metadata_path = iteration_dir / "iteration_metadata.json"
    metadata = read_json_utf8(metadata_path) if metadata_path.exists() else {}
    baseline = infer_baseline(iteration_dir, metadata)

    pairs: list[RunPair] = []

    eval_dirs = sorted(
        (path for path in iteration_dir.glob("eval-*") if path.is_dir()),
        key=lambda path: parse_numeric_suffix(path, "eval-"),
    )
    if not eval_dirs:
        raise FileNotFoundError(f"No eval directories found in {iteration_dir}")

    for eval_dir in eval_dirs:
        eval_metadata_path = eval_dir / "eval_metadata.json"
        eval_metadata = read_json_utf8(eval_metadata_path) if eval_metadata_path.exists() else {}
        eval_id = int(eval_metadata.get("eval_id", parse_numeric_suffix(eval_dir, "eval-")))
        eval_name = str(eval_metadata.get("eval_name", f"eval-{eval_id}"))

        config_map = {
            path.name: path
            for path in eval_dir.iterdir()
            if path.is_dir() and any(child.is_dir() for child in path.glob("run-*"))
        }
        if "with_skill" not in config_map:
            raise ValueError(f"Missing with_skill config in {eval_dir}")
        if baseline not in config_map:
            raise ValueError(f"Missing baseline config '{baseline}' in {eval_dir}")

        run_specs_by_number: dict[int, list[RunSpec]] = {}
        for config_name in sorted(("with_skill", baseline), key=config_sort_key):
            for run_dir in sorted(
                config_map[config_name].glob("run-*"),
                key=lambda path: parse_numeric_suffix(path, "run-"),
            ):
                if not run_dir.is_dir():
                    continue
                run_number = parse_numeric_suffix(run_dir, "run-")
                prompt_path = run_dir / "run_prompt.md"
                if not prompt_path.exists():
                    raise FileNotFoundError(f"run_prompt.md not found: {prompt_path}")

                spec = RunSpec(
                    eval_id=eval_id,
                    eval_name=eval_name,
                    config=config_name,
                    run_number=run_number,
                    run_dir=run_dir,
                    prompt_path=prompt_path,
                    baseline_type=baseline,
                )
                if resume and is_successful_run(spec):
                    continue
                run_specs_by_number.setdefault(run_number, []).append(spec)

        for run_number in sorted(run_specs_by_number):
            specs = sorted(run_specs_by_number[run_number], key=lambda item: config_sort_key(item.config))
            if not specs:
                continue
            pairs.append(
                RunPair(
                    eval_id=eval_id,
                    eval_name=eval_name,
                    run_number=run_number,
                    runs=tuple(specs),
                )
            )

    pairs.sort(key=lambda item: (item.run_number, item.eval_id))
    return pairs


def write_launch_failure(spec: RunSpec, error: Exception, command: list[str], baseline_type: str) -> None:
    end_time = utc_now()
    timing = {
        "total_tokens": 0,
        "duration_ms": 0,
        "total_duration_seconds": 0.0,
        "executor_start": isoformat_utc(end_time),
        "executor_end": isoformat_utc(end_time),
        "executor_duration_seconds": 0.0,
        "exit_code": -1,
        "status": "launch_failed",
        "error": str(error),
    }
    write_json_utf8(spec.timing_path, timing)

    provenance = {
        "executor": "codex exec",
        "configuration": spec.config,
        "baseline_type": baseline_type,
        "skill_path_requested": discover_skill_path(spec.run_dir),
        "prompt_path": str(spec.prompt_path),
        "command": command,
        "status": "launch_failed",
        "error": str(error),
        "recorded_at": isoformat_utc(end_time),
    }
    write_json_utf8(spec.provenance_path, provenance)


def launch_run(
    spec: RunSpec,
    codex_bin: str,
    model: str,
    reasoning_effort: str | None,
    sandbox: str,
) -> ActiveRun:
    command, resolved_reasoning_effort = build_codex_command(
        codex_bin=codex_bin,
        run_dir=spec.run_dir,
        model=model,
        reasoning_effort=reasoning_effort,
        sandbox=sandbox,
    )
    spec.run_dir.mkdir(parents=True, exist_ok=True)

    prompt_handle = spec.prompt_path.open("rb")
    stdout_handle = spec.events_path.open("wb")
    stderr_handle = spec.stderr_path.open("wb")
    start_dt = utc_now()
    start_perf = time.perf_counter()

    try:
        process = subprocess.Popen(
            command,
            stdin=prompt_handle,
            stdout=stdout_handle,
            stderr=stderr_handle,
            cwd=spec.run_dir,
        )
    except Exception as error:
        prompt_handle.close()
        stdout_handle.close()
        stderr_handle.close()
        write_launch_failure(spec, error, command, spec.baseline_type)
        raise

    return ActiveRun(
        spec=spec,
        command=command,
        resolved_reasoning_effort=resolved_reasoning_effort,
        process=process,
        prompt_handle=prompt_handle,
        stdout_handle=stdout_handle,
        stderr_handle=stderr_handle,
        start_dt=start_dt,
        start_perf=start_perf,
    )


def finalize_run(active_run: ActiveRun) -> CompletedRun:
    returncode = active_run.process.wait()
    end_dt = utc_now()
    elapsed_seconds = max(time.perf_counter() - active_run.start_perf, 0.0)
    total_tokens = extract_tokens_from_events(active_run.spec.events_path)

    active_run.prompt_handle.close()
    active_run.stdout_handle.close()
    active_run.stderr_handle.close()

    timing = {
        "total_tokens": total_tokens,
        "duration_ms": int(round(elapsed_seconds * 1000)),
        "total_duration_seconds": round(elapsed_seconds, 3),
        "executor_start": isoformat_utc(active_run.start_dt),
        "executor_end": isoformat_utc(end_dt),
        "executor_duration_seconds": round(elapsed_seconds, 3),
        "exit_code": returncode,
        "status": "completed" if returncode == 0 else "failed",
    }
    write_json_utf8(active_run.spec.timing_path, timing)

    provenance = {
        "executor": "codex exec",
        "configuration": active_run.spec.config,
        "baseline_type": active_run.spec.baseline_type,
        "skill_path_requested": discover_skill_path(active_run.spec.run_dir),
        "prompt_path": str(active_run.spec.prompt_path),
        "command": active_run.command,
        "model": next(
            (active_run.command[index + 1] for index, value in enumerate(active_run.command[:-1]) if value == "--model"),
            None,
        ),
        "reasoning_effort": active_run.resolved_reasoning_effort,
        "events_path": str(active_run.spec.events_path),
        "stderr_path": str(active_run.spec.stderr_path),
        "exit_code": returncode,
        "status": "completed" if returncode == 0 else "failed",
        "recorded_at": isoformat_utc(end_dt),
    }
    write_json_utf8(active_run.spec.provenance_path, provenance)

    return CompletedRun(
        spec=active_run.spec,
        returncode=returncode,
        duration_seconds=elapsed_seconds,
        total_tokens=total_tokens,
    )


def print_queue_plan(pairs: list[RunPair]) -> None:
    print(f"Queued {len(pairs)} pair(s):")
    for pair in pairs:
        labels = ", ".join(spec.label for spec in pair.runs)
        print(f"  {pair.label}: {labels}")


def run_pairs(
    pairs: list[RunPair],
    codex_bin: str,
    model: str,
    reasoning_effort: str | None,
    sandbox: str,
    max_concurrency: int,
    poll_interval: float,
) -> list[CompletedRun]:
    max_required_slots = max((pair.size for pair in pairs), default=0)
    if max_required_slots > max_concurrency:
        raise ValueError(
            f"max_concurrency={max_concurrency} is too small for a pair requiring {max_required_slots} slot(s)"
        )

    pending = deque(pairs)
    active: list[ActiveRun] = []
    completed: list[CompletedRun] = []

    while pending or active:
        finished: list[ActiveRun] = []
        for active_run in active:
            if active_run.process.poll() is not None:
                finished.append(active_run)

        for active_run in finished:
            active.remove(active_run)
            result = finalize_run(active_run)
            completed.append(result)
            status = "ok" if result.returncode == 0 else f"exit={result.returncode}"
            print(
                f"Completed {result.spec.label} in {result.duration_seconds:.2f}s "
                f"({status}, tokens={result.total_tokens})"
            )

        while pending:
            available_slots = max_concurrency - len(active)
            next_pair = pending[0]
            if next_pair.size > available_slots:
                break

            pending.popleft()
            print(f"Launching {next_pair.label} with {next_pair.size} run(s)")
            launched_runs: list[ActiveRun] = []
            try:
                for spec in next_pair.runs:
                    launched_runs.append(
                        launch_run(
                            spec=spec,
                            codex_bin=codex_bin,
                            model=model,
                            reasoning_effort=reasoning_effort,
                            sandbox=sandbox,
                        )
                    )
            except Exception:
                for active_run in launched_runs:
                    active_run.process.terminate()
                    finalize_run(active_run)
                raise

            active.extend(launched_runs)

        if active:
            time.sleep(poll_interval)

    return completed


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Run benchmark iterations via bounded rolling queue"
    )
    parser.add_argument(
        "iteration_dir",
        type=Path,
        help="Path to the iteration directory created by setup_benchmark.py",
    )
    parser.add_argument(
        "--codex-bin",
        default="codex",
        help="Codex executable to invoke (default: codex)",
    )
    parser.add_argument(
        "--model",
        default=DEFAULT_MODEL,
        help=f"Model to use for codex exec (default: {DEFAULT_MODEL})",
    )
    parser.add_argument(
        "--reasoning-effort",
        help="Optional reasoning effort override passed to Codex",
    )
    parser.add_argument(
        "--sandbox",
        default=DEFAULT_SANDBOX,
        choices=("read-only", "workspace-write", "danger-full-access"),
        help=f"Sandbox mode for codex exec (default: {DEFAULT_SANDBOX})",
    )
    parser.add_argument(
        "--max-concurrency",
        type=int,
        default=DEFAULT_MAX_CONCURRENCY,
        help=f"Maximum number of live codex exec processes (default: {DEFAULT_MAX_CONCURRENCY})",
    )
    parser.add_argument(
        "--poll-interval",
        type=float,
        default=DEFAULT_POLL_INTERVAL,
        help=f"Seconds between polling active runs (default: {DEFAULT_POLL_INTERVAL})",
    )
    parser.add_argument(
        "--resume",
        action="store_true",
        help="Skip runs that already have timing.json with exit_code 0",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print the planned pair queue without launching codex exec",
    )

    args = parser.parse_args()

    try:
        iteration_dir = args.iteration_dir.resolve()
        if not iteration_dir.exists():
            raise FileNotFoundError(f"Iteration directory not found: {iteration_dir}")

        max_concurrency = validate_max_concurrency(args.max_concurrency)
        poll_interval = validate_poll_interval(args.poll_interval)
        codex_bin = resolve_codex_bin(args.codex_bin)
        pairs = discover_pairs(iteration_dir, resume=args.resume)
    except Exception as error:
        print(f"Error: {error}")
        sys.exit(1)

    if not pairs:
        print("No pending benchmark runs found.")
        return

    print_queue_plan(pairs)
    if args.dry_run:
        return

    try:
        completed = run_pairs(
            pairs=pairs,
            codex_bin=codex_bin,
            model=args.model,
            reasoning_effort=args.reasoning_effort,
            sandbox=args.sandbox,
            max_concurrency=max_concurrency,
            poll_interval=poll_interval,
        )
    except Exception as error:
        print(f"Error: {error}")
        sys.exit(1)

    successes = sum(1 for item in completed if item.returncode == 0)
    failures = len(completed) - successes
    print("")
    print("Run summary:")
    print(f"  Completed runs: {len(completed)}")
    print(f"  Succeeded:      {successes}")
    print(f"  Failed:         {failures}")
    if failures:
        sys.exit(1)


if __name__ == "__main__":
    main()
